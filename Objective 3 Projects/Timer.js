//*displays countdown of timer*\\
function Time1()
{   var currTime = 10; 
    var i = 1;
    for (var i = 1; i < 12; i++)
   while (i <12)
    
        {setTimeout(function () {
            document.getElementById("Timer").innerHTML = "the time left is " + currTime;
            currTime = currTime - 1;
               }, 1000 * i);
     i = i+1;  //this equals i++;
    }

}


